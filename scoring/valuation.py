def compute_base_value(position: str, market: str, tier: str, output_score: float,
                       tier_cutoffs: tuple[float, float, float]) -> int:
    """
    Compute base NIL value by interpolating within the range for (position, market, tier).

    Interpolation: where the player's output_score falls within their tier's score range
    determines where they fall within the dollar range.

    Returns integer dollar value.
    """
    pos_ranges = NIL_RANGES.get(position)
    if pos_ranges is None:
        return 0

    market_ranges = pos_ranges.get(market)
    if market_ranges is None:
        return 0

    tier_range = market_ranges.get(tier)
    if tier_range is None:
        return 0

    val_min, val_max = tier_range
    t1_min, t2_min, t3_min = tier_cutoffs

    # Determine the score range for this tier
    if tier == 'T1':
        score_floor = t1_min
        score_ceil = 100.0
    elif tier == 'T2':
        score_floor = t2_min
        score_ceil = t1_min
    elif tier == 'T3':
        score_floor = t3_min
        score_ceil = t2_min
    else:  # T4
        score_floor = 0.0
        score_ceil = t3_min

    # Interpolate within the range
    score_range = score_ceil - score_floor
    if score_range <= 0:
        pct = 0.5
    else:
        pct = max(0.0, min(1.0, (output_score - score_floor) / score_range))

    value = val_min + pct * (val_max - val_min)
    return round(value)
